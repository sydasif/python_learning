# Math Operator in Python
num_1 = 3
num_2 = 2

result = num_1 + num_2
print(result)

result = num_1 - num_2
print(result)

result = num_1 * num_2
print(result)

result = num_1 / num_2 # when divide a number, the result will b float
print(result)

result = num_1 // num_2 # floor division
print(result)

result = num_1 ** num_2 # exponent
print(result)

result = num_1 % num_2 # divide and print reminder
print(result)